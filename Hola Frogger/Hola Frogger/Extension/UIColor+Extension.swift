import UIKit

extension UIColor {
    static func raspberryPieTint() -> UIColor {
        return #colorLiteral(red: 0.8078431487, green: 0.02745098062, blue: 0.3333333433, alpha: 1)
    }
    static func wavesTint() -> UIColor {
        return #colorLiteral(red: 0.4943358898, green: 0.8269905448, blue: 0.1309139729, alpha: 1)
    }
    static func lottieTint() -> UIColor {
        return #colorLiteral(red: 0.9791167378, green: 0.8544659019, blue: 0.7218489051, alpha: 1)
    }
}
